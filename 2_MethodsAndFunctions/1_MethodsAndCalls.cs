public class MethodsAndCalls
{
    public static void Run()
    {
        // Run() örneği başlatır ve yardımcı metodu çağırır.
        CallMethod();
    }

    static void CallMethod()
    {
        // Metot çağrısı ile konsola basit bir mesaj yazdırıyoruz.
        Console.WriteLine("Merhaba, ben geldim.");
    }
}